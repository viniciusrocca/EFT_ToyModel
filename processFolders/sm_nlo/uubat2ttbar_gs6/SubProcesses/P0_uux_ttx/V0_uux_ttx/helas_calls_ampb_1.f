      SUBROUTINE HELAS_CALLS_AMPB_1(P,NHEL,H,IC)
C     
C     Modules
C     
      USE POLYNOMIAL_CONSTANTS
C     
      IMPLICIT NONE
C     
C     CONSTANTS
C     
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=4)
      INTEGER    NCOMB
      PARAMETER (NCOMB=16)
      INTEGER NBORNAMPS
      PARAMETER (NBORNAMPS=1)
      INTEGER    NLOOPS, NLOOPGROUPS, NCTAMPS
      PARAMETER (NLOOPS=11, NLOOPGROUPS=8, NCTAMPS=36)
      INTEGER    NLOOPAMPS
      PARAMETER (NLOOPAMPS=47)
      INTEGER    NWAVEFUNCS,NLOOPWAVEFUNCS
      PARAMETER (NWAVEFUNCS=6,NLOOPWAVEFUNCS=26)
      REAL*8     ZERO
      PARAMETER (ZERO=0D0)
      REAL*16     MP__ZERO
      PARAMETER (MP__ZERO=0.0E0_16)
C     These are constants related to the split orders
      INTEGER    NSO, NSQUAREDSO, NAMPSO
      PARAMETER (NSO=2, NSQUAREDSO=1, NAMPSO=2)
C     
C     ARGUMENTS
C     
      REAL*8 P(0:3,NEXTERNAL)
      INTEGER NHEL(NEXTERNAL), IC(NEXTERNAL)
      INTEGER H
C     
C     LOCAL VARIABLES
C     
      INTEGER I,J,K
      COMPLEX*16 COEFS(MAXLWFSIZE,0:VERTEXMAXCOEFS-1,MAXLWFSIZE)

      LOGICAL DUMMYFALSE
      DATA DUMMYFALSE/.FALSE./
C     
C     GLOBAL VARIABLES
C     

      INCLUDE 'coupl.inc'
      INCLUDE 'mp_coupl.inc'

      INTEGER HELOFFSET
      INTEGER GOODHEL(NCOMB)
      LOGICAL GOODAMP(NSQUAREDSO,NLOOPGROUPS)
      COMMON/FILTERS/GOODAMP,GOODHEL,HELOFFSET

      LOGICAL CHECKPHASE
      LOGICAL HELDOUBLECHECKED
      COMMON/INIT/CHECKPHASE, HELDOUBLECHECKED

      INTEGER SQSO_TARGET
      COMMON/SOCHOICE/SQSO_TARGET

      LOGICAL UVCT_REQ_SO_DONE,MP_UVCT_REQ_SO_DONE,CT_REQ_SO_DONE
     $ ,MP_CT_REQ_SO_DONE,LOOP_REQ_SO_DONE,MP_LOOP_REQ_SO_DONE
     $ ,CTCALL_REQ_SO_DONE,FILTER_SO
      COMMON/SO_REQS/UVCT_REQ_SO_DONE,MP_UVCT_REQ_SO_DONE
     $ ,CT_REQ_SO_DONE,MP_CT_REQ_SO_DONE,LOOP_REQ_SO_DONE
     $ ,MP_LOOP_REQ_SO_DONE,CTCALL_REQ_SO_DONE,FILTER_SO

      INTEGER I_SO
      COMMON/I_SO/I_SO
      INTEGER I_LIB
      COMMON/I_LIB/I_LIB

      COMPLEX*16 AMP(NBORNAMPS)
      COMMON/AMPS/AMP
      COMPLEX*16 W(20,NWAVEFUNCS)
      COMMON/W/W

      COMPLEX*16 WL(MAXLWFSIZE,0:LOOPMAXCOEFS-1,MAXLWFSIZE,
     $ -1:NLOOPWAVEFUNCS)
      COMPLEX*16 PL(0:3,-1:NLOOPWAVEFUNCS)
      COMMON/WL/WL,PL

      COMPLEX*16 AMPL(3,NCTAMPS)
      COMMON/AMPL/AMPL

C     
C     ----------
C     BEGIN CODE
C     ----------

C     The target squared split order contribution is already reached
C      if true.
      IF (FILTER_SO.AND.CT_REQ_SO_DONE) THEN
        GOTO 1001
      ENDIF

      CALL IXXXXX(P(0,1),ZERO,NHEL(1),+1*IC(1),W(1,1))
      CALL OXXXXX(P(0,2),ZERO,NHEL(2),-1*IC(2),W(1,2))
      CALL OXXXXX(P(0,3),MDL_MT,NHEL(3),+1*IC(3),W(1,3))
      CALL IXXXXX(P(0,4),MDL_MT,NHEL(4),-1*IC(4),W(1,4))
      CALL FFV1P0_3(W(1,1),W(1,2),GC_12,ZERO,ZERO,W(1,5))
C     Amplitude(s) for born diagram with ID 1
      CALL FFV1_0(W(1,4),W(1,3),W(1,5),GC_12,AMP(1))
      CALL FFV1P0_3(W(1,4),W(1,3),GC_12,ZERO,ZERO,W(1,6))
C     Counter-term amplitude(s) for loop diagram number 2
      CALL VV1_3_0(W(1,5),W(1,6),R2GC_156_39,R2GC_155_38,AMPL(1,1))
      CALL VV1_0(W(1,5),W(1,6),UVGC_156_57_1EPS,AMPL(2,2))
C     Counter-term amplitude(s) for loop diagram number 3
      CALL FFV1_0(W(1,4),W(1,3),W(1,5),R2GC_120_21,AMPL(1,3))
      CALL FFV1_4_0(W(1,4),W(1,3),W(1,5),UVGC_120_21_1EPS
     $ ,UVGC_171_101_1EPS,AMPL(2,4))
      CALL FFV4_0(W(1,4),W(1,3),W(1,5),UVGC_123_24_1EPS,AMPL(2,5))
      CALL FFV4_0(W(1,4),W(1,3),W(1,5),UVGC_123_25_1EPS,AMPL(2,6))
      CALL FFV4_0(W(1,4),W(1,3),W(1,5),UVGC_123_25_1EPS,AMPL(2,7))
      CALL FFV4_0(W(1,4),W(1,3),W(1,5),UVGC_123_25_1EPS,AMPL(2,8))
      CALL FFV4_0(W(1,4),W(1,3),W(1,5),UVGC_123_25_1EPS,AMPL(2,9))
      CALL FFV4_0(W(1,4),W(1,3),W(1,5),UVGC_123_26_1EPS,AMPL(2,10))
      CALL FFV4_0(W(1,4),W(1,3),W(1,5),UVGC_123_27_1EPS,AMPL(2,11))
      CALL FFV4_0(W(1,4),W(1,3),W(1,5),UVGC_123_28_1EPS,AMPL(2,12))
      CALL FFV4_0(W(1,4),W(1,3),W(1,5),UVGC_171_101,AMPL(1,13))
C     Counter-term amplitude(s) for loop diagram number 5
      CALL FFV1_0(W(1,1),W(1,2),W(1,6),R2GC_120_21,AMPL(1,14))
      CALL FFV1_4_0(W(1,1),W(1,2),W(1,6),UVGC_120_21_1EPS
     $ ,UVGC_123_29_1EPS,AMPL(2,15))
      CALL FFV4_0(W(1,1),W(1,2),W(1,6),UVGC_123_24_1EPS,AMPL(2,16))
      CALL FFV4_0(W(1,1),W(1,2),W(1,6),UVGC_123_25_1EPS,AMPL(2,17))
      CALL FFV4_0(W(1,1),W(1,2),W(1,6),UVGC_123_25_1EPS,AMPL(2,18))
      CALL FFV4_0(W(1,1),W(1,2),W(1,6),UVGC_123_25_1EPS,AMPL(2,19))
      CALL FFV4_0(W(1,1),W(1,2),W(1,6),UVGC_123_25_1EPS,AMPL(2,20))
      CALL FFV4_0(W(1,1),W(1,2),W(1,6),UVGC_123_26_1EPS,AMPL(2,21))
      CALL FFV4_0(W(1,1),W(1,2),W(1,6),UVGC_123_27_1EPS,AMPL(2,22))
      CALL FFV4_0(W(1,1),W(1,2),W(1,6),UVGC_123_28_1EPS,AMPL(2,23))
C     Counter-term amplitude(s) for loop diagram number 9
      CALL VV1_0(W(1,5),W(1,6),UVGC_156_58_1EPS,AMPL(2,24))
C     Counter-term amplitude(s) for loop diagram number 10
      CALL VV3_0(W(1,5),W(1,6),R2GC_155_37,AMPL(1,25))
      CALL VV3_0(W(1,5),W(1,6),R2GC_155_37,AMPL(1,26))
      CALL VV3_0(W(1,5),W(1,6),R2GC_155_37,AMPL(1,27))
      CALL VV3_0(W(1,5),W(1,6),R2GC_155_37,AMPL(1,28))
C     Counter-term amplitude(s) for loop diagram number 11
      CALL VV2_0(W(1,5),W(1,6),R2GC_87_60,AMPL(1,29))
      CALL VV3_0(W(1,5),W(1,6),R2GC_155_37,AMPL(1,30))
      CALL VV1_3_0(W(1,5),W(1,6),UVGC_156_56_1EPS,UVGC_155_54_1EPS
     $ ,AMPL(2,31))
      CALL VV1_3_0(W(1,5),W(1,6),UVGC_156_56,UVGC_155_54,AMPL(1,32))
C     Counter-term amplitude(s) for loop diagram number 12
      CALL VV2_0(W(1,5),W(1,6),R2GC_87_61,AMPL(1,33))
      CALL VV3_0(W(1,5),W(1,6),R2GC_155_37,AMPL(1,34))
      CALL VV1_3_0(W(1,5),W(1,6),UVGC_156_59_1EPS,UVGC_155_55_1EPS
     $ ,AMPL(2,35))
      CALL VV1_3_0(W(1,5),W(1,6),UVGC_156_59,UVGC_155_55,AMPL(1,36))
C     At this point, all CT amps needed for (QCD=6 QED=0), i.e. of
C      split order ID=1, are computed.
      IF(FILTER_SO.AND.SQSO_TARGET.EQ.1) GOTO 2000

      GOTO 1001
 2000 CONTINUE
      CT_REQ_SO_DONE=.TRUE.
 1001 CONTINUE
      END

