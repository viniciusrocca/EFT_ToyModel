      SUBROUTINE HELAS_CALLS_AMPB_1(P,NHEL,H,IC)
C     
C     Modules
C     
      USE POLYNOMIAL_CONSTANTS
C     
      IMPLICIT NONE
C     
C     CONSTANTS
C     
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=4)
      INTEGER    NCOMB
      PARAMETER (NCOMB=16)
      INTEGER NBORNAMPS
      PARAMETER (NBORNAMPS=1)
      INTEGER    NLOOPS, NLOOPGROUPS, NCTAMPS
      PARAMETER (NLOOPS=13, NLOOPGROUPS=10, NCTAMPS=45)
      INTEGER    NLOOPAMPS
      PARAMETER (NLOOPAMPS=58)
      INTEGER    NWAVEFUNCS,NLOOPWAVEFUNCS
      PARAMETER (NWAVEFUNCS=6,NLOOPWAVEFUNCS=31)
      REAL*8     ZERO
      PARAMETER (ZERO=0D0)
      REAL*16     MP__ZERO
      PARAMETER (MP__ZERO=0.0E0_16)
C     These are constants related to the split orders
      INTEGER    NSO, NSQUAREDSO, NAMPSO
      PARAMETER (NSO=5, NSQUAREDSO=2, NAMPSO=3)
C     
C     ARGUMENTS
C     
      REAL*8 P(0:3,NEXTERNAL)
      INTEGER NHEL(NEXTERNAL), IC(NEXTERNAL)
      INTEGER H
C     
C     LOCAL VARIABLES
C     
      INTEGER I,J,K
      COMPLEX*16 COEFS(MAXLWFSIZE,0:VERTEXMAXCOEFS-1,MAXLWFSIZE)

      LOGICAL DUMMYFALSE
      DATA DUMMYFALSE/.FALSE./
C     
C     GLOBAL VARIABLES
C     

      INCLUDE 'coupl.inc'
      INCLUDE 'mp_coupl.inc'

      INTEGER HELOFFSET
      INTEGER GOODHEL(NCOMB)
      LOGICAL GOODAMP(NSQUAREDSO,NLOOPGROUPS)
      COMMON/FILTERS/GOODAMP,GOODHEL,HELOFFSET

      LOGICAL CHECKPHASE
      LOGICAL HELDOUBLECHECKED
      COMMON/INIT/CHECKPHASE, HELDOUBLECHECKED

      INTEGER SQSO_TARGET
      COMMON/SOCHOICE/SQSO_TARGET

      LOGICAL UVCT_REQ_SO_DONE,MP_UVCT_REQ_SO_DONE,CT_REQ_SO_DONE
     $ ,MP_CT_REQ_SO_DONE,LOOP_REQ_SO_DONE,MP_LOOP_REQ_SO_DONE
     $ ,CTCALL_REQ_SO_DONE,FILTER_SO
      COMMON/SO_REQS/UVCT_REQ_SO_DONE,MP_UVCT_REQ_SO_DONE
     $ ,CT_REQ_SO_DONE,MP_CT_REQ_SO_DONE,LOOP_REQ_SO_DONE
     $ ,MP_LOOP_REQ_SO_DONE,CTCALL_REQ_SO_DONE,FILTER_SO

      INTEGER I_SO
      COMMON/I_SO/I_SO
      INTEGER I_LIB
      COMMON/I_LIB/I_LIB

      COMPLEX*16 AMP(NBORNAMPS)
      COMMON/AMPS/AMP
      COMPLEX*16 W(20,NWAVEFUNCS)
      COMMON/W/W

      COMPLEX*16 WL(MAXLWFSIZE,0:LOOPMAXCOEFS-1,MAXLWFSIZE,
     $ -1:NLOOPWAVEFUNCS)
      COMPLEX*16 PL(0:3,-1:NLOOPWAVEFUNCS)
      COMMON/WL/WL,PL

      COMPLEX*16 AMPL(3,NCTAMPS)
      COMMON/AMPL/AMPL

C     
C     ----------
C     BEGIN CODE
C     ----------

C     The target squared split order contribution is already reached
C      if true.
      IF (FILTER_SO.AND.CT_REQ_SO_DONE) THEN
        GOTO 1001
      ENDIF

      CALL IXXXXX(P(0,1),ZERO,NHEL(1),+1*IC(1),W(1,1))
      CALL OXXXXX(P(0,2),ZERO,NHEL(2),-1*IC(2),W(1,2))
      CALL OXXXXX(P(0,3),MDL_MT,NHEL(3),+1*IC(3),W(1,3))
      CALL IXXXXX(P(0,4),MDL_MT,NHEL(4),-1*IC(4),W(1,4))
      CALL FFV1P0_3(W(1,1),W(1,2),GC_11,ZERO,ZERO,W(1,5))
C     Amplitude(s) for born diagram with ID 1
      CALL FFV1_0(W(1,4),W(1,3),W(1,5),GC_11,AMP(1))
C     Counter-term amplitude(s) for loop diagram number 2
      CALL FFV3_0(W(1,4),W(1,3),W(1,5),R2GC_159_36,AMPL(1,1))
      CALL FFV3_0(W(1,4),W(1,3),W(1,5),UVGC_159_91_1EPS,AMPL(2,2))
      CALL FFV2_3_0(W(1,4),W(1,3),W(1,5),UVGC_156_87,UVGC_159_91
     $ ,AMPL(1,3))
C     At this point, all CT amps needed for (NP1=2 NP3=0 NP2=0 QCD=4
C      QED=0), i.e. of split order ID=1, are computed.
      IF(FILTER_SO.AND.SQSO_TARGET.EQ.1) GOTO 2000
      CALL FFV1P0_3(W(1,4),W(1,3),GC_11,ZERO,ZERO,W(1,6))
C     Counter-term amplitude(s) for loop diagram number 3
      CALL VV1_3_0(W(1,5),W(1,6),R2GC_137_23,R2GC_136_22,AMPL(1,4))
      CALL VV1_0(W(1,5),W(1,6),UVGC_137_27_1EPS,AMPL(2,5))
C     Counter-term amplitude(s) for loop diagram number 4
      CALL FFV1_0(W(1,4),W(1,3),W(1,5),R2GC_106_1,AMPL(1,6))
      CALL FFV1_7_0(W(1,4),W(1,3),W(1,5),UVGC_111_2_1EPS
     $ ,UVGC_156_86_1EPS,AMPL(2,7))
      CALL FFV7_0(W(1,4),W(1,3),W(1,5),UVGC_141_41_1EPS,AMPL(2,8))
      CALL FFV7_0(W(1,4),W(1,3),W(1,5),UVGC_141_41_1EPS,AMPL(2,9))
      CALL FFV7_0(W(1,4),W(1,3),W(1,5),UVGC_141_41_1EPS,AMPL(2,10))
      CALL FFV7_0(W(1,4),W(1,3),W(1,5),UVGC_141_41_1EPS,AMPL(2,11))
      CALL FFV7_0(W(1,4),W(1,3),W(1,5),UVGC_141_42_1EPS,AMPL(2,12))
      CALL FFV7_0(W(1,4),W(1,3),W(1,5),UVGC_141_43_1EPS,AMPL(2,13))
      CALL FFV7_0(W(1,4),W(1,3),W(1,5),UVGC_141_40,AMPL(1,14))
      CALL FFV7_0(W(1,4),W(1,3),W(1,5),UVGC_141_44,AMPL(1,15))
      CALL FFV7_0(W(1,4),W(1,3),W(1,5),UVGC_141_45,AMPL(1,16))
      CALL FFV7_0(W(1,4),W(1,3),W(1,5),UVGC_156_86,AMPL(1,17))
C     Counter-term amplitude(s) for loop diagram number 6
      CALL FFV1_0(W(1,1),W(1,2),W(1,6),R2GC_106_1,AMPL(1,18))
      CALL FFV1_7_0(W(1,1),W(1,2),W(1,6),UVGC_111_2_1EPS
     $ ,UVGC_141_46_1EPS,AMPL(2,19))
      CALL FFV7_0(W(1,1),W(1,2),W(1,6),UVGC_141_41_1EPS,AMPL(2,20))
      CALL FFV7_0(W(1,1),W(1,2),W(1,6),UVGC_141_41_1EPS,AMPL(2,21))
      CALL FFV7_0(W(1,1),W(1,2),W(1,6),UVGC_141_41_1EPS,AMPL(2,22))
      CALL FFV7_0(W(1,1),W(1,2),W(1,6),UVGC_141_41_1EPS,AMPL(2,23))
      CALL FFV7_0(W(1,1),W(1,2),W(1,6),UVGC_141_42_1EPS,AMPL(2,24))
      CALL FFV7_0(W(1,1),W(1,2),W(1,6),UVGC_141_43_1EPS,AMPL(2,25))
      CALL FFV7_0(W(1,1),W(1,2),W(1,6),UVGC_141_40,AMPL(1,26))
      CALL FFV7_0(W(1,1),W(1,2),W(1,6),UVGC_141_44,AMPL(1,27))
      CALL FFV7_0(W(1,1),W(1,2),W(1,6),UVGC_141_45,AMPL(1,28))
C     Counter-term amplitude(s) for loop diagram number 10
      CALL VV1_0(W(1,5),W(1,6),UVGC_137_28_1EPS,AMPL(2,29))
C     Counter-term amplitude(s) for loop diagram number 11
      CALL VV3_0(W(1,5),W(1,6),R2GC_136_21,AMPL(1,30))
      CALL VV3_0(W(1,5),W(1,6),R2GC_136_21,AMPL(1,31))
      CALL VV3_0(W(1,5),W(1,6),R2GC_136_21,AMPL(1,32))
      CALL VV3_0(W(1,5),W(1,6),R2GC_136_21,AMPL(1,33))
C     Counter-term amplitude(s) for loop diagram number 12
      CALL VV2_0(W(1,5),W(1,6),R2GC_108_3,AMPL(1,34))
      CALL VV3_0(W(1,5),W(1,6),R2GC_136_21,AMPL(1,35))
      CALL VV1_3_0(W(1,5),W(1,6),UVGC_137_26_1EPS,UVGC_136_23_1EPS
     $ ,AMPL(2,36))
      CALL VV1_3_0(W(1,5),W(1,6),UVGC_137_26,UVGC_136_23,AMPL(1,37))
C     Counter-term amplitude(s) for loop diagram number 13
      CALL VV2_0(W(1,5),W(1,6),R2GC_108_5,AMPL(1,38))
      CALL VV3_0(W(1,5),W(1,6),R2GC_136_21,AMPL(1,39))
      CALL VV1_3_0(W(1,5),W(1,6),UVGC_137_30_1EPS,UVGC_136_25_1EPS
     $ ,AMPL(2,40))
      CALL VV1_3_0(W(1,5),W(1,6),UVGC_137_30,UVGC_136_25,AMPL(1,41))
C     Counter-term amplitude(s) for loop diagram number 14
      CALL VV2_0(W(1,5),W(1,6),R2GC_108_4,AMPL(1,42))
      CALL VV3_0(W(1,5),W(1,6),R2GC_136_21,AMPL(1,43))
      CALL VV1_3_0(W(1,5),W(1,6),UVGC_137_29_1EPS,UVGC_136_24_1EPS
     $ ,AMPL(2,44))
      CALL VV1_3_0(W(1,5),W(1,6),UVGC_137_29,UVGC_136_24,AMPL(1,45))
C     At this point, all CT amps needed for (NP1=0 NP3=0 NP2=0 QCD=6
C      QED=0), i.e. of split order ID=2, are computed.
      IF(FILTER_SO.AND.SQSO_TARGET.EQ.2) GOTO 2000

      GOTO 1001
 2000 CONTINUE
      CT_REQ_SO_DONE=.TRUE.
 1001 CONTINUE
      END

